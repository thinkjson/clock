function loadImages(urls, callback) {
  const results = {};
  for (const name in urls) {
    map.loadImage(urls[name], makeCallback(name));
  }

  function makeCallback(name) {
    return (err, image) => {
      results[name] = err ? null : image;

      // if all images are loaded, call the callback
      if (Object.keys(results).length === Object.keys(urls).length) {
        callback(results);
      }
    };
  }
}

const images = {
  popup: "https://docs.mapbox.com/mapbox-gl-js/assets/popup.png",
  "popup-debug": "https://docs.mapbox.com/mapbox-gl-js/assets/popup_debug.png",
};

loadImages(images, (loadedImages) => {
  map.on("load", () => {
    map.addImage("popup", loadedImages["popup"], {
      stretchX: [
        [25, 55],
        [85, 115],
      ],
      stretchY: [[25, 100]],
      content: [25, 25, 115, 100],
      pixelRatio: 2,
    });

    map.addSource("points", {
      type: "geojson",
      data: {
        type: "FeatureCollection",
        features: Object.keys(places).map((name) => ({
          type: "Feature",
          geometry: {
            type: "Point",
            coordinates: places[name],
          },
          properties: {
            "image-name": "popup",
            name: `${name} ${times[name].format("h:mm a")}`,
          },
        })),
      },
    });

    map.addLayer({
      id: "points",
      type: "symbol",
      source: "points",
      layout: {
        "text-field": ["get", "name"],
        "icon-text-fit": "both",
        "icon-image": ["get", "image-name"],
        "icon-allow-overlap": true,
        "text-allow-overlap": true,
      },
    });
  });
});
